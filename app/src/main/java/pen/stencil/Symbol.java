package pen.stencil;

/**
 * Symbols for use as arrowheads, map symbols, etc.
 */
public enum Symbol {
    /** A solid triangular arrowhead filled with the foreground color. */
    SOLID_ARROW,

    /** A simple arrow head. */
    OPEN_ARROW,

    /** A small dot filled with the foreground color. */
    SOLID_DOT,

    /** A small dot filled with the background color. */
    OPEN_DOT;
}
